# Ввод трехзначного числа
number = int(input("Введите трехзначное число: "))

# Проверка, что число действительно трехзначное
if 100 <= number <= 999:
    # Получаем первую цифру (сотни)
    first_digit = number // 100

    # Получаем последние две цифры (десятки и единицы)
    last_two_digits = number % 100

    # Формируем новое число: последние две цифры + первая цифра
    new_number = last_two_digits * 10 + first_digit

    # Выводим результат
    print(f"Исходное число: {number}")
    print(f"Новое число: {new_number}")
else:
    print("Ошибка! Введите трехзначное число.")
